// models/TiffinCategory.js
const mongoose = require('mongoose');

const tiffinCategorySchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    price: {
        type: Number,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    imageUrl: {
        type: String,
        required: true
    }
});

module.exports = mongoose.model('TiffinCategory', tiffinCategorySchema);
